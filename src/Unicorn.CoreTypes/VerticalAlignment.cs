namespace Unicorn.CoreTypes
{
    /// <summary>
    /// The vertical alignment of drawable content within its container.
    /// </summary>
    public enum VerticalAlignment
    {
        /// <summary>
        /// Draw the content at the top of the container.
        /// </summary>
        Top,

        /// <summary>
        /// Draw the content centred vertically within the container.
        /// </summary>
        Centred,

        /// <summary>
        /// Draw the content with adjacent baselines aligned.
        /// </summary>
        Baseline,

        /// <summary>
        /// Draw the content at the bottom of the container.
        /// </summary>
        Bottom
    }
}
